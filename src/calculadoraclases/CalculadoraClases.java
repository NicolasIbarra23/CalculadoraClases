
package calculadoraclases;

public class CalculadoraClases
{
    public float suma (float num1, float num2)
    {
        float resultado = 0;
        resultado = num1 + num2;
        return resultado;
    }
    
    public float resta(float num1, float num2)
    {
        float resultado = 0;
        resultado = num1 - num2;
        return resultado;
    }
    
     public float mult(float num1, float num2)
    {
        float resultado = 0;
        resultado = num1 * num2;
        return resultado;
    }
     
      public float div (float num1, float num2)
    {
        float resultado = 0;
        resultado = num1 / num2;
        return resultado;
    }
       public double sen(double num1)
    {
        double resultado = 0;
        resultado = Math.sin(num1);
        return resultado;
    }
    
        public double cos(double num1)
    {
        double resultado = 0;
        resultado = Math.cos(num1);
        return resultado;
    }
    
         public double tan(double num1)
    {
        double resultado = 0;
        resultado = Math.tan(num1);
        return resultado;
    }
          public float inv(float num1)
    {
        float resultado = 0;
        resultado = 1 / num1;
        return resultado;
    }
}

